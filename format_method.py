# Format Method

text = "{}, I am a {} programmer and I {} Clarusway.".format("Hello", "new", "love")
print(text)

text = "{0}, I am a {1} programmer and I {2} Clarusway.".format("Hello", "new", "love")
print(text)

toygar = "Merhaba, {2} adım {1}. {3} bu kodu {0} yazarım arkadaş.".format("eksiksiz", "Toygar", "benim", "Ben")
print(toygar)

toygar = "Merhaba, {} adım {}. {} bu kodu {} yazarım arkadaş.".format("benim", "Toygar", "Ben", "eksiksiz")
print(toygar)


